public class Strings {
    public static void main(String[] args) {
        // Sua string de exemplo
        String minhaString = "Exemplo de String";

        // Obtém a substring do índice 5 ao 7
        String parteDaString = minhaString.substring(5, 8);

        // Imprime a substring
        System.out.println("Substring: " + parteDaString);
    }
}
